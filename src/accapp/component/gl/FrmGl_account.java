    private void validasi() throws CodeException {
        if (!jtxtaccountgroupcode.getText().equals("")) {
            Gl_group glp = new Gl_group(jtxtaccountgroupcode.getText());

            if(glp.getID() == Gl_group.NULLID) {
                throw new CodeException("Account Group " + jtxtaccountgroupcode.getText() + " Tidak Diketemukan");
            }

        }
        if (!jtxtaccountgroupcode.getText().equals("")) {
            Gl_account_structure glacst = new Gl_account_structure(jtxtabrkcode.getText());

            if(glacst.getID() == Gl_group.NULLID) {
                throw new CodeException("Account Structure " + jtxtabrkcode.getText() + " Tidak Diketemukan");
            }

        }
        if (!jtxtacccode.getText().equals("")) {
            Gl_account glacc = new Gl_account(jtxtacccode.getText());

            if(glacc.getID() == Gl_group.NULLID) {
                int jwb=JOptionPane.showConfirmDialog(this, "Account Code " + jtxtacccode.getText() + " Tidak Diketemukan, Tambah Baru?", "Save Data", JOptionPane.YES_NO_OPTION);
                if (jwb==JOptionPane.YES_OPTION){
                    obj.setID(obj.NULLID);
                } else {
                    undo();
                }
            }

        }

    }

