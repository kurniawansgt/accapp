    public void showData() throws CodeException {
            setCursor(GlobalUtils.HOURGLASSCURSOR);
	
             jtxtid.setText(obj.toString(Gl_account.PROPERTY_ID));
             jtxtacccode.setText(obj.toString(Gl_account.PROPERTY_ACCCODE));

             jtxtaccfmttd.setText(obj.toString(Gl_account.PROPERTY_ACCFMTTD));
             
             jtxtaccdesc.setText(obj.toString(Gl_account.PROPERTY_ACCDESC));
             
             if (obj.getaccbal() != null && obj.getaccbal().equals("1")) {
                 jrdrDebit.setSelected(true);
             }else{
                 jrdrCredit.setSelected(true);
             }

             jtxtabrkcode.setText(obj.toString(Gl_account.PROPERTY_ABRKCODE));
             
             if (obj.getacctype() != null && obj.getacctype().equals("I")) {
                 jrdrIncome.setSelected(true);
             }else if (obj.getacctype() != null && obj.getacctype().equals("B")) {
                 jrdrBalanceSheet.setSelected(true);
             }else{
                 jrdrRetainedEarning.setSelected(true);
             }

             jtxtaccountgroupcode.setText(obj.getaccgrpcode()+"");
             jtxtAccountGroupDescription.setText(obj.getAccountGroupDescription());
             
             jtxtentrydate.setText(obj.toString(Gl_account.PROPERTY_ENTRYDATE));

             if (obj.toString(Gl_account.PROPERTY_ACTIVEW) != null && obj.toString(Gl_account.PROPERTY_ACTIVEW).equalsIgnoreCase("1")){
                 jrdrActive.setSelected(true);
             } else {
                 jrdrInactive.setSelected(true);
             }
             jtxtauditdate.setText(obj.toString(Gl_account.PROPERTY_AUDITDATE));
             jtxtaudituser.setText(obj.toString(Gl_account.PROPERTY_AUDITUSER));
             jtxtcmpnyid.setText(obj.toString(Gl_account.PROPERTY_CMPNYID));

             setCursor(GlobalUtils.NORMALCURSOR);
    }

