    public void setData() throws CodeException {
        
	
          obj.fromString(Gl_account.PROPERTY_ACCCODE, jtxtacccode.getText()) ;
          obj.fromString(Gl_account.PROPERTY_ACCFMTTD, jtxtaccfmttd.getText()) ;

          obj.fromString(Gl_account.PROPERTY_ACCDESC, jtxtaccdesc.getText()) ;

          if (jrdrDebit.isSelected()) {
              obj.fromString(Gl_account.PROPERTY_ACCBAL, "1");
          }else{
              obj.fromString(Gl_account.PROPERTY_ACCBAL, "2");
          }

          obj.fromString(Gl_account.PROPERTY_ABRKCODE, jtxtabrkcode.getText()) ;

          if(jrdrIncome.isSelected()) {
              obj.fromString(Gl_account.PROPERTY_ACCTYPE, "I");
          }else if(jrdrBalanceSheet.isSelected()) {
              obj.fromString(Gl_account.PROPERTY_ACCTYPE, "B");
          }else {
              obj.fromString(Gl_account.PROPERTY_ACCTYPE, "R");
          }

          if (istatus==1){
              obj.fromString(Gl_account.PROPERTY_ENTRYDATE, GlobalUtils.getAuditDate()) ;
          }
          if (jrdrActive.isSelected()){
              obj.fromString(Gl_account.PROPERTY_ACTIVEW, "1") ;
          } else {
              obj.fromString(Gl_account.PROPERTY_ACTIVEW, "0") ;
          }

          obj.setaccgrpcode(jtxtaccountgroupcode.getText());

          obj.fromString(Gl_account.PROPERTY_AUDITDATE, GlobalUtils.getAuditDate()) ;
          obj.fromString(Gl_account.PROPERTY_AUDITUSER, GlobalUtils.userid) ;
          obj.fromString(Gl_account.PROPERTY_CMPNYID, GlobalUtils.company) ;

    }

