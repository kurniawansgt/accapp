    private void jbtnBrowseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnBrowseActionPerformed
        // TODO add your handling code here:
        try {
          Gl_fiscalhs brs = new Gl_fiscalhs();
          FrmBrowse bd = new FrmBrowse( brs);
          bd.setFirstValue(jtxtyop);
          bd.setModal(true);
          bd.setVisible(true);
          obj = new Gl_fiscalh(Gl_fiscalh.PROPERTY_YOP, jtxtyop.getText());
          jComboBox1.removeAllItems();
          for (Object o :obj.getDetail().list()){
              Gl_fiscald od=(Gl_fiscald) o;
              jComboBox1.addItem(od.getmopdesc());
          }

        } catch (CodeException ex) {
          ex.printStackTrace();
        }
    }//GEN-LAST:event_jbtnBrowseActionPerformed

    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        jtxtyop.setText(GlobalUtils.formatDate(GlobalUtils.sessiondate, "yyyy"));
        try {
            obj = new Gl_fiscalh(Gl_fiscalh.PROPERTY_YOP, jtxtyop.getText());
            jComboBox1.removeAllItems();
            for (Object o :obj.getDetail().list()){
              Gl_fiscald od=(Gl_fiscald) o;
              jComboBox1.addItem(od.getmopdesc());
            }
        } catch (CodeException ex) {
            Logger.getLogger(FrmAP_aging_process.class.getName()).log(Level.SEVERE, null, ex);
        }


        jComboBox1.setSelectedIndex(GlobalUtils.toInt(GlobalUtils.formatDate(GlobalUtils.sessiondate, "MM"))-1);
    }//GEN-LAST:event_formInternalFrameOpened


