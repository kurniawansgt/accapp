    private void addNumber(String number){
        jtxtNumber.setEnabled(true);
        jtxtNumber.setEditable(false);

        if (jtxtNumber.getText().equals("0") || jtxtNumber.getText().equals("0.0") 
                || tombolbefore.equals("+")
                || tombolbefore.equals("-")
                || tombolbefore.equals("/")
                || tombolbefore.equals("*")
                ){
            jtxtNumber.setText("");
        }

        if(number.equals(".")){
            if (findDot(jtxtNumber.getText())) {
                number = "";
            }
        }
        
        tombolbefore = number;
        jtxtNumber.setText(jtxtNumber.getText()+number);
    }

    private boolean findDot(String string) {
        boolean find = false;
        char character[] = string.toCharArray();

        for(int i=0; i < character.length; i++){
            if (character[i] == '.' ) {
                find = true;
                break;
            }
        }

        return find;
    }

    private void clear(){
        number1 = 0;
        result = 0;
        jtxtNumber.setText(result+"");
        tombol = "";
    }


    private double calc(double val1, String operator , double val2){
        
        double resultcalc = 0;
        if (operator.equals("+")){
            resultcalc = val1 + val2;
        }else if (operator.equals("-")){
            resultcalc = val1 - val2;
        }else if (operator.equals("*")){
            resultcalc = val1 * val2;
        }else if (operator.equals("/")){
            resultcalc = val1 / val2;
        }else{
            resultcalc = val2;
        }

        return resultcalc;

    }

    private void viewResult() {
        result = calc(number1, tombol, Double.parseDouble(jtxtNumber.getText()));
        jtxtNumber.setText(result+"");
        result = 0;
        number1 = 0;
        tombol = "";
        
    }


