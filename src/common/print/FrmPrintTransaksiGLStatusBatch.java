private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed

        
    try{
        setCursor(GlobalUtils.HOURGLASSCURSOR);
        String file   = "" ;
        Map parameter = new HashMap();    
        parameter.put("batchnofr", jtxtNoBatchFr.getText());
        parameter.put("batchnoto", jtxtNoBatchTo.getText());
        parameter.put("operator",GlobalUtils.userid);
        parameter.put("namaPT", (GlobalUtils.getNamaPT()));
        String open="";
        String posted="";
        String salah="";
        String hapus="";
        String pposted="";
        if(jchkopen.isSelected()){
             open="1";
        }else{
            open="O";
        }
        if(jchkposted.isSelected()){
             posted="2";
        }else{
            posted="P";
        }
        if(jchkerror.isSelected()){
            salah="3";
        }else{
            salah="E";
        }
        if(jchkdelete.isSelected()){
             hapus="4";
        }else{
            hapus="D";
        }
      if(jchkprovposted.isSelected()){
            pposted="5";
        }else{
            pposted="R";
        }
        parameter.put("status1", open);
        parameter.put("status2", posted);
        parameter.put("status3", salah);
        parameter.put("status4", hapus);
        parameter.put("status5", pposted);

        parameter.put("entried", jchkenter.isSelected() ? "1" :"E");
        parameter.put("subledger", jchkposted.isSelected() ? "2" :"S");
        parameter.put("imported", jchkimport.isSelected() ? "3" :"I");
        parameter.put("transfer", jchktransfer.isSelected() ? "4" :"T");

        System.out.println(open);
        System.out.println(posted);
        System.out.println(salah);
        System.out.println(hapus);
        System.out.println(pposted);
        System.out.println(jchkenter.isSelected() ? "1" : "E");
        System.out.println(jchkposted.isSelected() ? "2" : "S");
        System.out.println(jchkimport.isSelected() ? "3" : "I");
        System.out.println(jchktransfer.isSelected() ? "4" : "T");
        try{

               String tgl1 = dateformatprint.format(dateformat.parse(batchdatefr.getDate()));
               String tgl2 = dateformatprint.format(dateformat.parse(batchdateto.getDate()));
               System.out.println(tgl1);
               System.out.println(tgl2);
               parameter.put("batchdatefro",tgl1);
               parameter.put("batchdatetoo",tgl2);
        }catch (Exception e ) {
        }
         
        file = GlobalUtils.getCurrentDir() +"/Reports/TransaksiGLStatusBatchList.jrxml";
        GlobalUtils.printReport(file, parameter, false);
    }catch (Exception e) {
        e.printStackTrace();
    }
    setCursor(GlobalUtils.NORMALCURSOR);
}//GEN-LAST:event_jbtnCetakActionPerformed

