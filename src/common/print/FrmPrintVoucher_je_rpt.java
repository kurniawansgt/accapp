private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed
        

    setCursor(GlobalUtils.HOURGLASSCURSOR);
    String file = "" ;

    Map parameter = new HashMap();

    parameter.put("batchid", jtxtNoBatchFr.getText());
    parameter.put("btchentrydate_fr", GlobalUtils.formatDate(kdateentrydate.getDate(),"yyyy-MM-dd"));
    parameter.put("btchentrydate_to", GlobalUtils.formatDate(kdateentrydate1.getDate(),"yyyy-MM-dd"));
    parameter.put("btchentry_fr", jtxtbtchentry_fr.getText());
    parameter.put("btchentry_to", jtxtbtchentry_to.getText());

    String allsourceglcode="N";
    if (jchckallsourceglcode.isSelected()){
            allsourceglcode = "Y";
    }
    parameter.put("allsourceglcode", allsourceglcode);
    parameter.put("srcelgrtypecode", jtxtsrcelgrtypecode.getText());
    parameter.put("audituser", GlobalUtils.getAuditUser());

    System.out.println(parameter);
    file = GlobalUtils.getCurrentDir() +"/Reports/voucher_je_rpt.jrxml";


    GlobalUtils.printReport(file, parameter, false);


    
    setCursor(GlobalUtils.NORMALCURSOR);
}//GEN-LAST:event_jbtnCetakActionPerformed

