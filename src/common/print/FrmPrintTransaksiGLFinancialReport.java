private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed
        try {
            obj = new Setting_financial_rpt(Setting_financial_rpt.PROPERTY_REPORTNAME,jtxtNoBatchFr.getText());
        } catch (CodeException ex) {
            Logger.getLogger(FrmPrintTransaksiGLFinancialReport.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            setCursor(GlobalUtils.HOURGLASSCURSOR);
            if (obj.getID() > 0) {
                if (obj.doProses(GlobalUtils.toInt(jtxtyop.getText()),jcmbBulan.getSelectedIndex()+1)){
                    try{
                        String file   = "" ;
                        Map parameter = new HashMap();

                        String mope=(jcmbBulan.getSelectedIndex()+1+"" );
                        String mopsbl="";
                        parameter.put("mop", mope);
                        parameter.put("yop", jtxtyop.getText());
                        String yopsbl="";
                        if (mope.equals("1")){
                            mopsbl="12";
                            parameter.put("mopsbl", mopsbl);
                            Integer yope=Integer.parseInt(jtxtyop.getText())-1;
                            yopsbl=yope.toString()  ;
                            parameter.put("yopsbl", yopsbl);
                        }else{
                            mopsbl=(jcmbBulan.getSelectedIndex()+"" );
                            parameter.put("mopsbl", mopsbl);
                            yopsbl=jtxtyop.getText() ;
                            parameter.put("yopsbl", yopsbl);
                        }
                        parameter.put("jdlrpt",jtxtNoBatchFr.getText());
                        parameter.put("operator",GlobalUtils.userid);
                        parameter.put("namaPT", (GlobalUtils.getNamaPT()));
                        
                        System.out.println(mope);
                        System.out.println(jtxtyop.getText());
                        System.out.println(mopsbl);
                        System.out.println(yopsbl);
                        System.out.println(jtxtNoBatchFr.getText());
                        System.out.println(GlobalUtils.userid);


                        file = GlobalUtils.getCurrentDir() +"/Reports/TransaksiGLFinancialReport.jrxml";
                        GlobalUtils.printReport(file, parameter, false);

                    }catch (Exception e) {
                        e.printStackTrace();
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Error Processing Data");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Report setting cannot be found");
            }
        } catch (CodeException ex) {
            Logger.getLogger(FrmPrintTransaksiGLFinancialReport.class.getName()).log(Level.SEVERE, null, ex);
        }
    setCursor(GlobalUtils.NORMALCURSOR);
}//GEN-LAST:event_jbtnCetakActionPerformed

