private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed

        
    try{
        Map parameter = new HashMap();
        String dayscyclfr="";
        String dayscyclto="";
        parameter.put("kodeperusahaan", " ");
        parameter.put("operator", (GlobalUtils.getUserId()));
        parameter.put("cyclcodefr", jtxtNoBatchFr.getText());
        parameter.put("cyclcodeto", jtxtNoBatchTo.getText());
        parameter.put("dayscyclfr", jtxtdaycyclfr.getText());
        parameter.put("dayscyclto", jtxtdaycyclto.getText());
        String Aktiffr="";
        if(jcmbaktiffr.getSelectedIndex()==0 ){
            Aktiffr="0";
        }else {
            Aktiffr="1";
        }
        parameter.put("aktiffr", Aktiffr);
        String Aktifto="";
        if(jcmbaktifto.getSelectedIndex()==0 ){
            Aktifto="1";
        }else {
            Aktifto="0";
        }
        parameter.put("aktifto", Aktifto);

        String file   = "";
        System.out.println(parameter);
        file = GlobalUtils.getCurrentDir() +"/Reports/MasterARBillCycle.jrxml";
        GlobalUtils.printReport(file, parameter, false);
    }catch (Exception e) {
        e.printStackTrace();
    }
}//GEN-LAST:event_jbtnCetakActionPerformed

