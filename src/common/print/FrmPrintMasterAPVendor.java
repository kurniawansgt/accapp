private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed
        
    try{
        Map parameter = new HashMap();
        parameter.put("operator", (GlobalUtils.getUserId()));
        parameter.put("vendcodefr", jtxtVendCodeFr.getText());
        parameter.put("vendcodeto", jtxtVendCodeTo.getText());
        parameter.put("yopfr", jtxtyopfr.getText());
        parameter.put("yopto", jtxtyopfr.getText());
        int status;
        if(jcmbStatus.getSelectedIndex()==0){
            status=1;
        }else{
            status=0;
        }
        parameter.put("active",status);
        String jenisreport="";
        if(jcmbjenis.getSelectedIndex()==0){
            jenisreport="0";
        }else{
            jenisreport="1";
        }
        parameter.put("jenislaporan",jenisreport);
        System.out.println(parameter);
        String file   ;
        file = GlobalUtils.getCurrentDir() +"/Reports/MasterAPVendor.jrxml";
        GlobalUtils.printReport(file, parameter, false); 
    }catch (Exception e) {
        e.printStackTrace();
    }
}//GEN-LAST:event_jbtnCetakActionPerformed

