private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed

        
    try{
        setCursor(GlobalUtils.HOURGLASSCURSOR);
        String file   = "" ;
        Map parameter = new HashMap();
        
        parameter.put("namaPT", (GlobalUtils.getNamaPT()));
        String afr=(jtxtAccCodeFr.getText());
        String ato=(jtxtAccCodeTo.getText());
        String a6fr=" " ;
        String a6to=" " ;
        if(afr.isEmpty() && ato.isEmpty()){
            a6fr="";
            a6to="";
        }else{
            String a1fr="" ;
            String a2fr="" ;
            String a3fr="" ;
            String a4fr="" ;
            String a5fr="" ;
            String a1to="";
            String a2to="" ;
            String a3to="";
            String a4to="" ;
            String a5to="" ;
            
            a1fr=afr.substring(0, 1) ;
            a2fr=afr.substring(2, 5) ;
            a3fr=afr.substring(6, 8) ;
            a4fr=afr.substring(9, 12) ;
            a5fr=afr.substring(13, 16) ;
            a1to=ato.substring(0, 1) ;
            a2to=ato.substring(2, 5) ;
            a3to=ato.substring(6, 8) ;
            a4to=ato.substring(9, 12) ;
            a5to=ato.substring(13, 16) ;
            a6fr=afr.replace("-","");
            a6to=ato.replace("-","");

        }
        parameter.put("acccodefr", a6fr);
        parameter.put("acccodeto", a6to);
        
        /* Segment Akun */
        parameter.put("akun1from",jTextAkun1From.getText());
        parameter.put("akun1to",jTextAkun1To.getText());
        parameter.put("akun2from",jTextAkun2From.getText());
        parameter.put("akun2to",jTextAkun2To.getText());
        parameter.put("akun3from",jTextAkun3From.getText());
        parameter.put("akun3to",jTextAkun3To.getText());
        parameter.put("akun4from",jTextAkun4From.getText());
        parameter.put("akun4to",jTextAkun4To.getText());
        parameter.put("akun5from",jTextAkun5From.getText());
        parameter.put("akun5to",jTextAkun5To.getText());

        parameter.put("operator",GlobalUtils.userid);

        parameter.put("mop", jcmbBulan.getSelectedIndex()+1);
        parameter.put("mop2", jcmbBulan2.getSelectedIndex()+1);
        parameter.put("yop", jtxtyop.getText());

        System.out.println(parameter);

        file = GlobalUtils.getCurrentDir() +"/Reports/TransaksiGLByAkun.jrxml";
        GlobalUtils.printReport(file, parameter, false);
    }catch (Exception e) {
        e.printStackTrace();
    }
    setCursor(GlobalUtils.NORMALCURSOR);
}//GEN-LAST:event_jbtnCetakActionPerformed

