private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed
        
    try{
        Map parameter = new HashMap();
        parameter.put("user_1", (GlobalUtils.getUserId()));
        
        Masterkodesetting mks = new Masterkodesetting();
        mks.loadData("RPT", "HEADERRPT");
        parameter.put("header", mks.getDescription());
        
        String file ="";
        if (jmbModel.getSelectedIndex() == 0) {
            parameter.put("user", jtxtKodeUser.getText());
            file   = GlobalUtils.getCurrentDir() +"/Reports/Masteruser.jrxml";           
        }else{                      
                file = GlobalUtils.getCurrentDir() +"/Reports/Daftaruser.jrxml";            
                        
        }
        
        GlobalUtils.printReport(file, parameter, false); 
    }catch (Exception e) {
        e.printStackTrace();
    }
}//GEN-LAST:event_jbtnCetakActionPerformed

