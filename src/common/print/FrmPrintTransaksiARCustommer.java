private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed

        
    try{
        String file   = "" ;
        Map parameter = new HashMap();
        parameter.put("vendorfr", jtxtVendCodeFr.getText());
        parameter.put("vendorto", jtxtVendCodeTo.getText());
        System.out.println(jtxtVendCodeFr.getText());
        System.out.println(jtxtVendCodeTo.getText());
        parameter.put("cutoffby",jcmbCuttOf.getSelectedIndex()+"");
        parameter.put("operator",GlobalUtils.userid);
        parameter.put("namaPT", (GlobalUtils.getNamaPT()));
        
       if(jcmbCuttOf.getSelectedIndex()==1){
            try{
                   String tgl1 = dateformatprint.format(dateformat.parse(batchdatefr.getDate()));
                   String tgl2 = dateformatprint.format(dateformat.parse(batchdateto.getDate()));
                   System.out.println(tgl1);
                   System.out.println(tgl2);
                   parameter.put("docdatefr",tgl1);
                   parameter.put("docdateto",tgl2);
            }catch (Exception e ) {
            }
        }else{
            if (jcmbCuttOf.getSelectedIndex()==2){
                    Integer mope=(jcmbBulan.getSelectedIndex())+1;
                    String mon="";
                    if (mope < 9 ){
                        mon='0'+mope.toString();
                    }else{
                        mon=mope.toString();
                    }
                    System.out.println(mon);
                    parameter.put("mop", mon );
                    parameter.put("yop", jtxtyop.getText());
                    System.out.println(jtxtyop.getText());
            }

        }

        String invoi="";
        String creditnot="";
        String debitnot="";
        String interest="";
        if(jchkopen.isSelected()){
            invoi="4";
        }else{
            invoi="IN";
        }
        if(jchkposted.isSelected()){
            creditnot="5";
        }else{
            creditnot="CN";
        }
        if(jchkerror.isSelected()){
            debitnot="6";
        }else{
            debitnot="DN";
        }
        if(jchkdelete.isSelected()){
            interest="7";
        }else{
            interest="IT";
        }
        parameter.put("doctypein", invoi);
        parameter.put("doctypecn", creditnot);
        parameter.put("doctypedn", debitnot);
        parameter.put("doctypeit", interest);

      System.out.println(parameter);
        
      if(jcmbCuttOf.getSelectedIndex()==1){
           file = GlobalUtils.getCurrentDir() +"/Reports/TransaksiARCustomerDocDate.jrxml";
        }else{
            if(jcmbCuttOf.getSelectedIndex()==2){
                   file = GlobalUtils.getCurrentDir() +"/Reports/TransaksiARCustomerYopMop.jrxml";
            }else{
                JOptionPane.showMessageDialog(this, "Print Error. Please choose your Cut Of By for Print Properly");
            }

        }

       GlobalUtils.printReport(file, parameter, false);
    }catch (Exception e) {
        e.printStackTrace();
    }
}//GEN-LAST:event_jbtnCetakActionPerformed

