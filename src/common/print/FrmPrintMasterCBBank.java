private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed

        
    try{
        Map parameter = new HashMap();
        parameter.put("kodeperusahaan", " ");
        parameter.put("operator", (GlobalUtils.getUserId()));
        parameter.put("banknofr", jtxtNoBatchFr.getText());
        parameter.put("banknoto", jtxtNoBatchTo.getText());
        String ReportType="";
        if(jcmbmopfr.getSelectedIndex()==0 ){
            ReportType="Address";
        }else if(jcmbmopfr.getSelectedIndex()==1){
            ReportType="Bank";
        }else{
            ReportType="Account Code";
        }
        parameter.put("jenisreport", ReportType);

        String file   = "";
        System.out.println(parameter);
        file = GlobalUtils.getCurrentDir() +"/Reports/MasterCBBank.jrxml";
        GlobalUtils.printReport(file, parameter, false);
    }catch (Exception e) {
        e.printStackTrace();
    }
}//GEN-LAST:event_jbtnCetakActionPerformed

