private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed

        
    try{
        setCursor(GlobalUtils.HOURGLASSCURSOR);
        String file   = "" ;
        Map parameter = new HashMap();
        parameter.put("namaPT", (GlobalUtils.getNamaPT()));
        parameter.put("operator",GlobalUtils.userid);

        if(jcmbCuttOf.getSelectedIndex()==1){
            String mope = "";
            if((jcmbBulan.getSelectedIndex()+1 ) >9){
               mope=(jcmbBulan.getSelectedIndex()+1+"" );
            }else{
               mope="0"+(jcmbBulan.getSelectedIndex()+1+"" );
            }
            parameter.put("bulan", mope);
            parameter.put("tahun", jtxtyop.getText());
            System.out.println(parameter);
            file = GlobalUtils.getCurrentDir() +"/Reports/TransaksiCBRegisterPeriode.jrxml";
            GlobalUtils.printReport(file, parameter, false);
        }else if(jcmbCuttOf.getSelectedIndex()==2){
            String mope = "";
            if((jcmbBulan.getSelectedIndex()+1 ) >9){
               mope=(jcmbBulan.getSelectedIndex()+1+"" );
            }else{
               mope="0"+(jcmbBulan.getSelectedIndex()+1+"" );
            }
            parameter.put("bankfr", jtxtBankFr.getText());
            parameter.put("bankto", jtxtBankTo.getText());
            parameter.put("bulan", mope);
            parameter.put("tahun", jtxtyop.getText());
            System.out.println(parameter);
            file = GlobalUtils.getCurrentDir() +"/Reports/TransaksiCBRegisterBank.jrxml";
            GlobalUtils.printReport(file, parameter, false);
        }else {
            JOptionPane.showMessageDialog(this, " Please select by Batch or Periode first. \n Thank You ");
        }

    }catch (Exception e) {
         e.printStackTrace();
    }
    setCursor(GlobalUtils.NORMALCURSOR);
}//GEN-LAST:event_jbtnCetakActionPerformed

