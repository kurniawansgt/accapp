private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed

        
    try{
        setCursor(GlobalUtils.HOURGLASSCURSOR);
        String file   = "" ;
        Map parameter = new HashMap();
        String bankcodefr=jtxtbankcodefr.getText();
        String bankcodeto=jtxtbankcodeto.getText();

        String batchnofr=jtxtbatchnofr.getText();
        String batchnoto=jtxtbatchnoto.getText();

        if(jtxtbankcodefr.getText().isEmpty()){
            bankcodefr="0";
        }
        if(jtxtbankcodeto.getText().isEmpty()){
            bankcodeto="ZZZZZZZZZ";
        }

        Integer mope=(jcmbBulan.getSelectedIndex())+1;
        String mon="";
        if (mope < 9 ){
            mon='0'+mope.toString();
        }else{
            mon=mope.toString();
        }
        
        parameter.put("yop", jtxtyop.getText());
        parameter.put("mop", mon );
        parameter.put("bankcodefr", bankcodefr);
        parameter.put("bankcodeto", bankcodeto);
        parameter.put("batchnofr", batchnofr);
        parameter.put("batchnoto", batchnoto);
        parameter.put("namaPT", (GlobalUtils.getNamaPT()));

        System.out.println(parameter);
        file = GlobalUtils.getCurrentDir() +"/Reports/cbregister.jrxml";


       GlobalUtils.printReport(file, parameter, false);
    }catch (Exception e) {
        e.printStackTrace();
    }
    setCursor(GlobalUtils.NORMALCURSOR);
}//GEN-LAST:event_jbtnCetakActionPerformed

