private void jbtnCetakActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCetakActionPerformed
        

    setCursor(GlobalUtils.HOURGLASSCURSOR);
    String file = "" ;

    Map parameter = new HashMap();

    String allstatus="N";
    if (jchckallsource.isSelected()){
            allstatus = "Y";
    }
    parameter.put("allstatus", allstatus);

    String advance_status="O";
    if (joptadvance_status_close.isSelected()){
        advance_status="C";
    }

    parameter.put("advance_status", advance_status);
    parameter.put("advance_date_fr", GlobalUtils.formatDate(kdateentrydate.getDate(),"yyyy-MM-dd"));
    parameter.put("advance_date_to", GlobalUtils.formatDate(kdateentrydate1.getDate(),"yyyy-MM-dd"));
    parameter.put("advance_no_fr", jtxtbtchentry_fr.getText());
    parameter.put("advance_no_to", jtxtbtchentry_to.getText());

    
    parameter.put("audituser", GlobalUtils.getAuditUser());

    System.out.println(parameter);

    String report_name="cb_cash_advance_rekap.jrxml";
    if (joptformat_by_detail.isSelected()){
        report_name="cb_cash_advance_detail.jrxml";
    }
    file = GlobalUtils.getCurrentDir() +"/Reports/"+report_name;


    GlobalUtils.printReport(file, parameter, false);


    
    setCursor(GlobalUtils.NORMALCURSOR);
}//GEN-LAST:event_jbtnCetakActionPerformed

